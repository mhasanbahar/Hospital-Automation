-- Duyurular Tablosu
create table TDuyurular (
    DuyuruID int primary key identity(1,1),
    Duyuru text
);

-- Randevular Tablosu
create table TRandevular(
	RandevuID int primary key identity(1,1),
    HastaID int,
    DoktorID int,
    RandevuTarih datetime,
    RandevuDurum int,
	HastaTCKimlikNo varchar(11), -- HastaTCKimlikNo sütunu eklendi
    HastaSikayet text, -- HastaSikayet sütunu eklendi
	RandevuBrans varchar(50),
	RandevuDoktor varchar(50),
	foreign key (HastaID) references THastalar(HastaID),
	foreign key (DoktorID) references TDoktorlar(DoktorID),
