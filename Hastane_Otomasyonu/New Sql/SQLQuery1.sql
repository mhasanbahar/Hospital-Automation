    HastaID int primary key identity(1,1),
    HastaAd varchar(50),
    HastaSoyad varchar(50),
    HastaTCKimlikNo varchar(11),
    HastaYas int,
    HastaCinsiyet varchar(10) ,
    HastaTelefonNo varchar(15),
    HastaEposta varchar(100),
    HastaSifre varchar(50),
