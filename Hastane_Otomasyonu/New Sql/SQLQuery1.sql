	SekreterID int primary key identity(1,1),
    SekreterAd varchar(50),
    SekreterSoyad varchar(50),
	SekreterTcKimlikNo varchar(11),
	SekreterYas int,
	SekreterCinsiyet varchar(10),
	SekreterTelefonNo varchar(15),
    SekreterEposta varchar(100),
